package Ch02;

public class C06실수오차범위확인 {

	public static void main(String[] args) {
		System.out.println("Float : " +(0.55555555555555555F));
		System.out.println("Float : " +(0.333333333333333333F));
		System.out.println("Double : " +(0.77777777777777777777777777777777));
		System.out.println("Double : " +(0.99999999999999999999999999999999));
		
		System.out.println((int)'ㄱ');
		System.out.println((int)'가');
		
		// double 15 ~18 자리까지 유효범위
	}

}
